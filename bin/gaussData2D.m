%Returns the gauss points and gauss weight. The values are in natural
% co-ordinates.
%Input: Order of integration.
%Created on: 7th Oct, 2016
classdef gaussData2D< handle
  properties
    pt
    wt
    noGp
  end
  methods
    
    
    function dat=gaussData2D(i)
      dat.noGp=i^2;
      switch(i)
        case 1
          dat.pt=[0,0];
          dat.wt=4;
        case 2
          dat.pt=[-1/3^0.5,-1/3^0.5;1/3^0.5,-1/3^0.5;1/3^0.5,1/3^0.5;-1/3^0.5,1/3^0.5];
          dat.wt=[1;1;1;1];
        case 3
          dat.pt=[-sqrt(3/5) -sqrt(3/5);
            -sqrt(3/5) 0;
            -sqrt(3/5) sqrt(3/5);
            0 -sqrt(3/5);
            0 0;
            0 sqrt(3/5);
            sqrt(3/5) -sqrt(3/5);
            sqrt(3/5) 0;
            sqrt(3/5) sqrt(3/5)];
          dat.wt=[   0.308641975308642;
            0.493827160493827;
            0.308641975308642;
            0.493827160493827;
            0.790123456790123;
            0.493827160493827;
            0.308641975308642;
            0.493827160493827;
            0.308641975308642];
        case 4
          dat.pt=[  -0.861136311594053  -0.861136311594053;
            -0.861136311594053  -0.339981043584856;
            -0.861136311594053   0.339981043584856;
            -0.861136311594053   0.861136311594053;
            -0.339981043584856  -0.861136311594053;
            -0.339981043584856  -0.339981043584856;
            -0.339981043584856   0.339981043584856;
            -0.339981043584856   0.861136311594053;
            0.339981043584856  -0.861136311594053;
            0.339981043584856  -0.339981043584856;
            0.339981043584856   0.339981043584856;
            0.339981043584856   0.861136311594053;
            0.861136311594053  -0.861136311594053;
            0.861136311594053  -0.339981043584856;
            0.861136311594053   0.339981043584856;
            0.861136311594053   0.861136311594053];
          dat.wt=[   0.121002993285602;
            0.226851851851852;
            0.226851851851852;
            0.121002993285602;
            0.226851851851852;
            0.425293303010694;
            0.425293303010694;
            0.226851851851852;
            0.226851851851852;
            0.425293303010694;
            0.425293303010694;
            0.226851851851852;
            0.121002993285602;
            0.226851851851852;
            0.226851851851852;
            0.121002993285602];
          
        otherwise
          msg = 'Invalid option: Order of integration greter than 4';
          error(msg);
      end
      
    end
  end
end